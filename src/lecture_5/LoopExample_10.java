package lecture_5;

public class LoopExample_10 {
    public static void main(String[] args) {
        int n = 0;
        while (n < 4)
        {
            int m = 0;


            while (m < 5)
            {
                System.out.print("А");
                m++;
            }


            System.out.println();
            n++;
        }
    }
}
